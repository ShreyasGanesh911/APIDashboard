import { toast ,Bounce} from "react-toastify";
import 'react-toastify/dist/ReactToastify.css';
export  const toastSuccess = (message:String)=>{
    toast.success(message, {
        position: "top-right",
        autoClose: 5000,
        hideProgressBar: true,
        closeOnClick: true,
        pauseOnHover: true,
        draggable: true,
        progress: undefined,
        theme: "dark",
        transition: Bounce,
        });
}

